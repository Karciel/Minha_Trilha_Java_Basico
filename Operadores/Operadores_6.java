public class Operadores_6 {
    public static void main(String[] args) {
        int numeroUm = 4;
        int numeroDois = 6;

        System.out.println("Eles são iguais: " + (numeroUm == numeroDois));

        System.out.println("São diferentes: " + (numeroUm != numeroDois));

        System.out.println("numeroUm é maior que numeroDois? - " + (numeroUm > numeroDois));

        System.out.println("numeroUm é igual ou maior que numeroDois - " + (numeroUm >= numeroDois));

        System.out.println("numeroUm é menor que numeroDois? - " + (numeroUm < numeroDois));

        System.out.println("numeroUm é igual ou menor que numeroDois? - " + (numeroUm <= numeroDois));


// Todos os operadores RELACIONAIS: 

/* 

==   (igual a)
!=   (diferente de)
>    (maior que)
>=   (maior ou igual a)
<    (menor que)
<=   (menor ou igual a)

*/
    }
}
